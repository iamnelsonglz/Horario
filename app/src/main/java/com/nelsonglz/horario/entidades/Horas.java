package com.nelsonglz.horario.entidades;

public class Horas {
    private String fecha;
    private String hentrada;
    private String hsalida;
    private String thoras;
    private String ahoras;

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }

    public String getHentrada() {
        return hentrada;
    }

    public void setHentrada(String hentrada) {
        this.hentrada = hentrada;
    }

    public String getHsalida() {
        return hsalida;
    }

    public void setHsalida(String hsalida) {
        this.hsalida = hsalida;
    }

    public String getThoras() {
        return thoras;
    }

    public void setThoras(String thoras) {
        this.thoras = thoras;
    }

    public String getAhoras() {
        return ahoras;
    }

    public void setAhoras(String ahoras) {
        this.ahoras = ahoras;
    }
}
